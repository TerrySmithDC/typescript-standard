export default class Calculator {
    public Add(a, b) {
        return a + b;
    }

    public Subtract(a, b) {
        return a - b;
    }

    public Divide(a: number, b: number): number {
        return a / b;
    }

    public Multiple(a: number, b: number): number {
        return a * b;
    }
}
